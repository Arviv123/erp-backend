// ─── getPortalSummary ─────────────────────────────────────────────────────────

/**
 * Dashboard summary for the portal customer.
 * All figures are derived from SENT/OVERDUE/PAID invoices and SENT/ACCEPTED quotes.
 */
export async function getPortalSummary(
  customerId: string,
  tenantId:   string
): Promise<PortalSummary> {
  const now = new Date();

  // Aggregate open (SENT + OVERDUE) and overdue invoices in one query.
  const [openAgg, overdueAgg] = await Promise.all([
    prisma.invoice.aggregate({
      where: {
        tenantId,
        customerId,
        status:    { in: ['SENT', 'OVERDUE'] },
        deletedAt: null,
      },
      _count: { id: true },
      _sum:   { total: true },
    }),
    prisma.invoice.aggregate({
      where: {
        tenantId,
        customerId,
        status:    'OVERDUE',
        deletedAt: null,
      },
      _count: { id: true },
      _sum:   { total: true },
    }),
  ]);

  // Most recent payment date for this customer's invoices.
  const lastPayment = await prisma.invoicePayment.findFirst({
    where: {
      tenantId,
      invoice: { customerId },
    },
    orderBy: { date: 'desc' },
    select:  { date: true },
  });

  // Active (SENT) quotes only — ACCEPTED ones are already committed.
  const activeQuotesCount = await prisma.quote.count({
    where: { tenantId, customerId, status: 'SENT' },
  });

  return {
    openInvoices:    openAgg._count.id,
    totalOwed:       Number(openAgg._sum.total ?? 0),
    overdueInvoices: overdueAgg._count.id,
    overdueAmount:   Number(overdueAgg._sum.total ?? 0),
    lastPaymentDate: lastPayment ? lastPayment.date.toISOString() : null,
    activeQuotes:    activeQuotesCount,
  };
}
