// ─── getPortalInvoices ────────────────────────────────────────────────────────

/**
 * Paginated list of invoices visible to the portal customer.
 * Only SENT, OVERDUE, and PAID statuses are shown.
 */
export async function getPortalInvoices(
  customerId: string,
  tenantId:   string,
  filters?: {
    status?: 'SENT' | 'OVERDUE' | 'PAID';
    page?:   number;
    limit?:  number;
  }
) {
  const page  = Math.max(1, filters?.page  ?? 1);
  const limit = Math.min(100, Math.max(1, filters?.limit ?? 20));
  const skip  = (page - 1) * limit;

  // Only these three statuses are visible to the customer.
  const allowedStatuses = ['SENT', 'OVERDUE', 'PAID'] as const;
  const statusFilter = filters?.status && allowedStatuses.includes(filters.status)
    ? [filters.status]
    : allowedStatuses;

  const where = {
    tenantId,
    customerId,
    status:    { in: statusFilter as any },
    deletedAt: null,
  };

  const [items, total] = await Promise.all([
    prisma.invoice.findMany({
      where,
      skip,
      take: limit,
      orderBy: { date: 'desc' },
      select: {
        id:          true,
        number:      true,
        invoiceType: true,
        date:        true,
        dueDate:     true,
        status:      true,
        subtotal:    true,
        vatAmount:   true,
        total:       true,
        currency:    true,
        notes:       true,
        paidAt:      true,
      },
    }),
    prisma.invoice.count({ where }),
  ]);

  return {
    items: items.map(inv => ({
      ...inv,
      subtotal:  Number(inv.subtotal),
      vatAmount: Number(inv.vatAmount),
      total:     Number(inv.total),
    })),
    total,
    page,
    limit,
    pages: Math.ceil(total / limit),
  };
}

// ─── getPortalInvoicePDF ──────────────────────────────────────────────────────

/**
 * Generates and returns a PDF Buffer for the given invoice.
 * Verifies the invoice belongs to the authenticated portal customer.
 */
export async function getPortalInvoicePDF(
  invoiceId:  string,
  customerId: string,
  tenantId:   string
): Promise<Buffer> {
  // Fetch invoice with ownership check.
  const invoice = await prisma.invoice.findFirst({
    where: {
      id:         invoiceId,
      customerId,
      tenantId,
      status:     { in: ['SENT', 'OVERDUE', 'PAID'] },
      deletedAt:  null,
    },
    include: {
      lines:    true,
      customer: { select: { name: true, email: true, phone: true, businessId: true, address: true } },
      tenant:   { select: { name: true, vatNumber: true, businessNumber: true, address: true, phone: true, email: true } },
    },
  });

  if (!invoice) {
    const err: any = new Error('Invoice not found');
    err.statusCode = 404;
    throw err;
  }

  const invoiceForPDF: InvoiceForPDF = {
    id:             invoice.id,
    number:         invoice.number,
    invoiceType:    invoice.invoiceType,
    date:           invoice.date,
    dueDate:        invoice.dueDate,
    status:         invoice.status,
    subtotal:       Number(invoice.subtotal),
    vatAmount:      Number(invoice.vatAmount),
    total:          Number(invoice.total),
    discountAmount: invoice.discountAmount ? Number(invoice.discountAmount) : null,
    notes:          invoice.notes,
    lines:          invoice.lines.map(l => ({
      description: l.description,
      quantity:    Number(l.quantity),
      unitPrice:   Number(l.unitPrice),
      vatRate:     Number(l.vatRate),
      lineTotal:   Number(l.lineTotal),
    })),
  };

  const tenantForPDF: TenantForPDF = {
    name:           invoice.tenant.name,
    vatNumber:      invoice.tenant.vatNumber,
    businessNumber: invoice.tenant.businessNumber,
    address:        invoice.tenant.address,
    phone:          invoice.tenant.phone,
    email:          invoice.tenant.email,
  };

  const customerForPDF: CustomerForPDF = {
    name:       invoice.customer.name,
    email:      invoice.customer.email,
    phone:      invoice.customer.phone,
    businessId: invoice.customer.businessId,
    address:    invoice.customer.address,
  };

  return generateInvoicePDF(invoiceForPDF, tenantForPDF, customerForPDF);
}

// ─── getPortalQuotes ──────────────────────────────────────────────────────────

/**
 * Returns all SENT or ACCEPTED quotes for the portal customer.
 */
export async function getPortalQuotes(customerId: string, tenantId: string) {
  const quotes = await prisma.quote.findMany({
    where: {
      tenantId,
      customerId,
      status: { in: ['SENT', 'ACCEPTED'] },
    },
    select: {
      id:         true,
      number:     true,
      title:      true,
      status:     true,
      issueDate:  true,
      expiryDate: true,
      subtotal:   true,
      vatAmount:  true,
      total:      true,
      notes:      true,
      termsConditions: true,
      lines: {
        select: {
          id:          true,
          description: true,
          quantity:    true,
          unitPrice:   true,
          discount:    true,
          lineTotal:   true,
          sortOrder:   true,
        },
        orderBy: { sortOrder: 'asc' },
      },
    },
    orderBy: { issueDate: 'desc' },
  });

  return quotes.map(q => ({
    ...q,
    subtotal:  Number(q.subtotal),
    vatAmount: Number(q.vatAmount),
    total:     Number(q.total),
    lines: q.lines.map(l => ({
      ...l,
      quantity:  Number(l.quantity),
      unitPrice: Number(l.unitPrice),
      discount:  Number(l.discount),
      lineTotal: Number(l.lineTotal),
    })),
  }));
}

// ─── getPortalPayments ────────────────────────────────────────────────────────

/**
 * Returns all payment records for invoices belonging to the portal customer.
 * Joins through the Invoice table to ensure ownership.
 */
export async function getPortalPayments(customerId: string, tenantId: string) {
  const payments = await prisma.invoicePayment.findMany({
    where: {
      tenantId,
      invoice: { customerId },
    },
    select: {
      id:        true,
      amount:    true,
      method:    true,
      date:      true,
      reference: true,
      notes:     true,
      createdAt: true,
      invoice:   { select: { id: true, number: true, total: true } },
    },
    orderBy: { date: 'desc' },
  });

  return payments.map(p => ({
    ...p,
    amount: Number(p.amount),
    invoice: { ...p.invoice, total: Number(p.invoice.total) },
  }));
}

// ─── acceptPortalQuote ────────────────────────────────────────────────────────

/**
 * Customer accepts a SENT quote.
 * Validates ownership by (quoteId + customerId + tenantId).
 */
export async function acceptPortalQuote(
  quoteId:    string,
  customerId: string,
  tenantId:   string
) {
  const quote = await prisma.quote.findFirst({
    where: { id: quoteId, customerId, tenantId },
    select: { id: true, status: true },
  });

  if (!quote) {
    const err: any = new Error('Quote not found');
    err.statusCode = 404;
    throw err;
  }

  if (quote.status !== 'SENT') {
    const err: any = new Error(`Cannot accept a quote with status ${quote.status}`);
    err.statusCode = 400;
    throw err;
  }

  return prisma.quote.update({
    where: { id: quoteId },
    data:  { status: 'ACCEPTED' },
    select: {
      id:     true,
      number: true,
      status: true,
      title:  true,
      total:  true,
    },
  });
}

